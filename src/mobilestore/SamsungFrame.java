    private void getEntries() {
        try {
            ResultSet rs = Database.DbConnect.st.executeQuery("Select * from Samsung");
            int sno = 0;
            DefaultTableModel dtm = (DefaultTableModel) table.getModel();

            table.getTableHeader().setFont(new Font("Roboto", Font.BOLD, 12));
            table.getTableHeader().setOpaque(false);
            table.getTableHeader().setBackground(Color.green);
            table.setRowHeight(30);

            int rows = dtm.getRowCount();
            while (rows-- != 0) {
                dtm.removeRow(0);
            }

            while (rs.next()) {
                String model = rs.getString("Model_Number");
                Long price = rs.getLong("Price");
                String color = rs.getString("Color");
                Long quantity = rs.getLong("Quantity");

                Object o[] = {++sno, model, color, price, quantity};
                dtm.addRow(o);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

