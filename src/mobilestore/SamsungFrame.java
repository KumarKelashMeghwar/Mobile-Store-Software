    private void removeButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeButtonMouseClicked
        String modelNumber = modelField.getText();
        int q;

        if (quantityField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Put a number in quantity field!");
            return;
        } else {
            q = Integer.parseInt(quantityField.getText());
        }

        try {
            ResultSet rs = Database.DbConnect.st.executeQuery("Select * from Samsung");
            boolean isFound = false;
            while (rs.next()) {
                String model = rs.getString("model_number");
                int quantity = rs.getInt("quantity");

                if (model.equals(modelNumber)) {
                    isFound = true;
                    if (q <= quantity) {
                        if (quantity > 0) {
                            Database.DbConnect.st.executeUpdate("update samsung set quantity=quantity-'" + q + "' where model_number = '" + modelNumber + "'");
                            JOptionPane.showMessageDialog(null, "Operation performed successfully!");
                            break;
                        } else if (quantity == 0 && q == 0) {
                            Database.DbConnect.st.executeUpdate("Delete from samsung where model_number='" + modelNumber + "'");
                            JOptionPane.showMessageDialog(null, "Operation performed successfully!");
                            break;
                        } else if (quantity == 0 && q >= 0) {
                            JOptionPane.showMessageDialog(null, "You can not remove because already mobiles quantity is 0!");
                            break;
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "You are removing more mobiles than available mobiles which is not possible!", "Warning", 2);
                    }
                }
            }

            if (isFound == false) {
                JOptionPane.showMessageDialog(null, "This model number does not exists in SAMSUNG!");
            }

            getEntries();
            setText();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_removeButtonMouseClicked

