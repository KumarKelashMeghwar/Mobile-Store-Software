    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        int row = table.getSelectedRow();

        if (row >= 0) {
            String model = (String) table.getValueAt(row, 1);
            String category = (String) table.getValueAt(row, 3);
            int items = (int) table.getValueAt(row, 2);

            try {
                int option = JOptionPane.showConfirmDialog(null, "Do you really want to remove this item from Cart?", "Confirmatio Message", JOptionPane.YES_NO_OPTION);

                if (option == JOptionPane.YES_OPTION) {
                    Database.DbConnect.st.executeUpdate("Delete from cartitems where model_number = '" + model + "' and category_name='" + category + "'");
                    Database.DbConnect.st.executeUpdate("update " + category + " set quantity=quantity + " + items + " where model_number='" + model + "'");
                    Database.DbConnect.st.executeUpdate("Delete from soldmobiles where model_number= '"+model+"' and category_name='"+category+"'");
                    getEntries();
                    setText();
                } else {
                    // do nothing!
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }


    }//GEN-LAST:event_jButton1MouseClicked

