    private void setText() {
        try {
            ResultSet rs = Database.DbConnect.st.executeQuery("Select * from cartitems");
            int amount = 0;
            while (rs.next()) {
                amount += rs.getLong("total_bill");

            }
            totalBillLabel.setText(String.valueOf(amount));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void getEntries() {
        DefaultTableModel dtm = (DefaultTableModel) table.getModel();

        int rows = dtm.getRowCount();

        while (rows-- != 0) {
            dtm.removeRow(0);
        }
        
       

        try {
            ResultSet rs = Database.DbConnect.st.executeQuery("Select * from cartitems");
            int sno = 0;
            while (rs.next()) {
                String model = rs.getString("model_number");
                String category = rs.getString("category_name");
                Long price = rs.getLong("total_bill");
                int items = rs.getInt("items");
                Object o[] = {++sno, model, items, category, price};
                dtm.addRow(o);
                
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }

