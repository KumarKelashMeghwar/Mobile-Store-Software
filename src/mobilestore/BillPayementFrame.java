    private void payNowBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_payNowBtnMouseClicked
        int payment = 0;
        if (enterAmountTextField.getText().equals("")) {
            // do nothing
        } else {
            payment = Integer.parseInt(enterAmountTextField.getText());
        }

        int totalBill = 0, remainingAmount;
        LocalDate dates = LocalDate.now();
        Object month = dates.getMonth();
        Object date = dates.getDayOfMonth();
        Object year = dates.getYear();
        String fullDate = date + "-" + month + "-" + year;

        try {
            ResultSet rs = Database.DbConnect.st.executeQuery("Select * from cartitems");
            while (rs.next()) {
                totalBill += rs.getInt("total_bill");

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        if (totalBill == 0) {
            JOptionPane.showMessageDialog(null, "Now, as your bill is 0. So, Please buy something and then pay your bill honestly thanks!!");
            return;
        }

        if (payment < totalBill || payment < 0) {
            JOptionPane.showMessageDialog(null, "You payment is less than you bill ... Please pay your bill honestly!");
            return;
        }
        if (payment == totalBill) {
            JOptionPane.showMessageDialog(null, "Your bill has been paid successfully! Thank you sir please come again!");

            try {
                ResultSet rs = Database.DbConnect.st.executeQuery("Select * from cartitems");

                while (rs.next()) {
                    String model = rs.getString("model_number");
                    String category = rs.getString("category_name");
                    Long price = rs.getLong("total_bill");
                    int items = rs.getInt("items");
                    Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe", "system", "chemicalBond");
                    Statement st = con.createStatement();
                    st.executeQuery("Insert into soldmobiles(model_number,items,category_name,total_bill,Buyer_Name,sal_date,mobile_number) "
                            + "values('" + model + "','" + items + "','" + category + "','" + price + "','" + Database.User.username + "','" + fullDate + "','" + Database.User.mobileNumber + "')");
                }
                Database.DbConnect.st.executeUpdate("truncate table cartitems");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
            return;
        }

        if (payment > totalBill) {
            remainingAmount = payment - totalBill;
            JOptionPane.showMessageDialog(null, "Your bill has been paid successfully! Your return amount is '" + remainingAmount + "' ! Thank you sir please come again!");
            try {
                ResultSet rs = Database.DbConnect.st.executeQuery("Select * from cartitems");

                while (rs.next()) {
                    String model = rs.getString("model_number");
                    String category = rs.getString("category_name");
                    Long price = rs.getLong("total_bill");
                    int items = rs.getInt("items");
                    Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe", "system", "chemicalBond");
                    Statement st = con.createStatement();
                    st.executeQuery("Insert into soldmobiles(model_number,items,category_name,total_bill,Buyer_Name,sal_date,mobile_number) "
                            + "values('" + model + "','" + items + "','" + category + "','" + price + "','" + Database.User.username + "','" + fullDate + "','" + Database.User.mobileNumber + "')");
                }
                Database.DbConnect.st.executeUpdate("truncate table cartitems");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }

    }//GEN-LAST:event_payNowBtnMouseClicked

