    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        int totalBill = 0;
        try {
            ResultSet rs = Database.DbConnect.st.executeQuery("Select * from cartitems");
            while (rs.next()) {
                int bill = rs.getInt("total_bill");
                totalBill += bill;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        if (totalBill != 0) {
            JOptionPane.showMessageDialog(null, "As you have not paid your bill so you can not close this until you pay or remove the item from your cart! Thanks!");
        } else {
            System.exit(0);
        }
    }//GEN-LAST:event_jLabel4MouseClicked

