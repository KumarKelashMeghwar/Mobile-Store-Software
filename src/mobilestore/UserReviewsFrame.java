    private void doneBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_doneBtnMouseClicked
        String reviewText;

        if (textArea.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please write something and then press 'done' button! Thanks.");
            return;
        } else {
            reviewText = textArea.getText();
        }

        try {
            Database.DbConnect.st.executeQuery("Insert into userreviews (name,mobile_number,review) values ('" + Database.User.username + "' , '" + Database.User.mobileNumber + "','" + reviewText + "')");
            JOptionPane.showMessageDialog(null, "Your review has been uploaded successfully!");
            getEntries();
            textArea.setText("");
        } 
        catch(SQLIntegrityConstraintViolationException e){
            JOptionPane.showMessageDialog(null, "Sir, You can only write one review, Thanks!");
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "You have crossed the limit... Maximum 500 characters can be written in the review!", "Warning", 2);
        }

    }//GEN-LAST:event_doneBtnMouseClicked

