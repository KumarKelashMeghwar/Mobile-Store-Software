    private void removeReviewBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeReviewBtnMouseClicked

        int row = table.getSelectedRow();

        if (row >= 0) {
            Long mobileNumber = Long.parseLong((String) table.getValueAt(row, 2));
            String name = (String) table.getValueAt(row, 1);
            
            if (Database.User.mobileNumber.equals(mobileNumber.toString())) {
                try {
                    int option = JOptionPane.showConfirmDialog(null, "Do you really wanna remove '" + name + "' ?", "Confirmation Message!", JOptionPane.YES_NO_OPTION);
                    if (option == JOptionPane.YES_OPTION) {
                        Database.DbConnect.st.executeUpdate("Delete from userreviews where mobile_number = '" + mobileNumber + "'");
                        JOptionPane.showMessageDialog(null, "Removed Successfully!");
                        getEntries();
                    } else {
                        // do nothing!
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            } else {
                JOptionPane.showMessageDialog(null, "You can only remove your own review, Thanks!", "Warning", 2);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please select any row which you wanna remove and then press remove button. Thanks!");
        }

    }//GEN-LAST:event_removeReviewBtnMouseClicked

